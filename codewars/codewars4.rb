def head(arr)
  arr.first
end

def tail(arr)
  arr.drop(1)
end

def init(arr)
  arr[0...-1]
end

def last(arr)
  arr.last
end


# p head([5,1])      # 5
# p tail([1])        # []
# p init([1,5,7,9])  # [1,5,7]
# p last([7,2])      # 2




def collatz(n)
  1 + (n == 1 ? 0 : collatz(n.even? ? n / 2 : n * 3 + 1))
end


