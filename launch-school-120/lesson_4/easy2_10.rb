# What are the benefits of using Object Oriented Programming in Ruby? Think of
# as many as you can.

# Benefits:
  # More translatable to plain English and thus easier to read and understand.
  # Better organization due to being able to separate logic into classes and
  #  modules.
  # Encapsulation - being able to group behaviour, attributes, and logic in
  #  clearly defined and separated locations.
  # Polymorphism - being able to call methods with the same name, but get
  #  different results, depending on the object that the method is called on.
  # Easier to refactor, add features, or otherwise modify code, because
  #  everything can be approached as a part of a whole.

# Book answer:
  # Creating objects allows programmers to think more abstractly about the code
  #  they are writing.
  # Objects are represented by nouns, so are easier to conceptualize.
  # Allows us to expose functionality to the parts of code that need it, meaning
  #  namespace issues are much harder to come across.
  # Allows us to easily give functionality to different parts of an app w/o
  #  duplication.
  # We can build apps faster as we can use pre-written code
  # As the software becomes more complex, this complexity can be more easily
  #  managed.
